package model;

/**
 *
 * @author Laptop88
 */
public class User_Quiz {
    private int id;
    private Quiz quiz;
    private User user;
    private int score;
    private int time;
    private int status;

    public User_Quiz() {
    }

    public User_Quiz(int id, Quiz quiz, User user, int score, int time, int status) {
        this.id = id;
        this.quiz = quiz;
        this.user = user;
        this.score = score;
        this.time = time;
        this.status = status;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Quiz getQuiz() {
        return quiz;
    }

    public void setQuiz(Quiz quiz) {
        this.quiz = quiz;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
    
    
    
}
